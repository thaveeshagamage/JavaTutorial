package methodOverloading;

public class JavaOverload {
	
	public int add(int a, int b) {
		
		return a+b;
	}
	
public int add(int a, int b, int c) {
		
		return a+b+c;
	}

public float add(float a, float b, float c) {
	
	return a+b+c;
}
public float add(float a, float b) {
	
	return a+b;
}
	

}
