package tutorialExcercises;

public class JavaRelationalOperators {
	public static void main(String[] Args) {
		
		int ex1 = 3;
		int ex2 = 6;
		
		boolean result;
		
		System.out.println("ex1 = 3 and ex2 = 6");
		
		//<operator
		result = ex1<ex2;
		System.out.println("ex1 < ex2 is " + result);
		
		//<operator
				result = ex1>ex2;
				System.out.println("ex1 > ex2 is " + result);
				
		//>=operator
				result = ex1>=ex2;
				System.out.println("ex1 >= ex2 is " + result);		

			//<=operator
				result = ex1<=ex2;
				System.out.println("ex1 <= ex2 is " + result);
	}

}
